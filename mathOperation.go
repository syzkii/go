package main

import "fmt"

func main() {

	var result = 20 + 20
	fmt.Println(result)

	var a = 20
	var b = 20
	var c = a * b
	fmt.Println(c)

	//Augmented Assignment

	var i = 20
	i += 20 // i = i + 10

	fmt.Println(i)

	i++ // i = i + 1
  //i++
	fmt.Println(i)

	var negative = -100
	var positive = +100

	fmt.Println(negative)
	fmt.Println(positive)

}